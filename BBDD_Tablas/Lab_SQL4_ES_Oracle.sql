DROP TABLE CLIENTE;
DROP TABLE CUENTA;
DROP TABLE PRESTAMO;
DROP TABLE SUCURSAL;


CREATE TABLE "SUCURSAL"
(
   NOMBRE_SUCURSAL varchar(15) NOT NULL,
   CIUDAD_SUCURSAL varchar(15) NOT NULL,
   ACTIVO decimal(12,3) NOT NULL,
   CONSTRAINT PK_SUCURSAL PRIMARY KEY (NOMBRE_SUCURSAL)
)
;

CREATE TABLE "CUENTA"
(
   NUM_CUENTA varchar(15) NOT NULL,
   NOMBRE_SUCURSAL varchar(15) NOT NULL,
   SALDO decimal(12,3) NOT NULL,
   CONSTRAINT PK_CUENTA PRIMARY KEY (NUM_CUENTA),
   CONSTRAINT FK_CUENTA_SUCURSAL FOREIGN KEY (NOMBRE_SUCURSAL) REFERENCES "SUCURSAL" (NOMBRE_SUCURSAL)
   
)
;

CREATE TABLE "PRESTAMO"
(
   NUM_PRESTAMO varchar(15) NOT NULL,
   NOMBRE_SUCURSAL varchar(15) NOT NULL,
   CANTIDAD decimal(12,3) NOT NULL,
   CONSTRAINT PK_PRESTAMO PRIMARY KEY (NUM_PRESTAMO),
   CONSTRAINT FK_PRESTAMO_SUCURSAL FOREIGN KEY (NOMBRE_SUCURSAL) REFERENCES "SUCURSAL" (NOMBRE_SUCURSAL)
)
;

CREATE TABLE "CLIENTE"
(
   NOMBRE_CLIENTE varchar(15) NOT NULL,
   CALLE_CLIENTE varchar(12) NOT NULL,
   CIUDAD_CLIENTE varchar(15) NOT NULL,
   CONSTRAINT PK_CLIENTE PRIMARY KEY (NOMBRE_CLIENTE)
)
;

CREATE TABLE "DEPOSITANTE"
(
   NOMBRE_CLIENTE varchar(15) NOT NULL,
   NUM_CUENTA varchar(15) NOT NULL,
   CONSTRAINT PK_DEPOSITANTE PRIMARY KEY (NOMBRE_CLIENTE,NUM_CUENTA),
   CONSTRAINT FK_DEPOSITANTE_CLIENTE FOREIGN KEY (NOMBRE_CLIENTE) REFERENCES "CLIENTE" (NOMBRE_CLIENTE),
   CONSTRAINT FK_DEPOSITANTE_CUENTA FOREIGN KEY (NUM_CUENTA) REFERENCES "CUENTA" (NUM_CUENTA)
)
;

CREATE TABLE "PRESTATARIO"
(
   NOMBRE_CLIENTE varchar(15) NOT NULL,
   NUM_PRESTAMO varchar(15) NOT NULL,
   CONSTRAINT PK_PRESTATARIO PRIMARY KEY (NOMBRE_CLIENTE,NUM_PRESTAMO),
   CONSTRAINT FK_PRESTATARIO_CLIENTE FOREIGN KEY (NOMBRE_CLIENTE) REFERENCES "CLIENTE" (NOMBRE_CLIENTE),
   CONSTRAINT FK_PRESTATARIO_PRESTAMO FOREIGN KEY (NUM_PRESTAMO) REFERENCES "PRESTAMO" (NUM_PRESTAMO)
)
;

